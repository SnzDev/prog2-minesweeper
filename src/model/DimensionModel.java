package model;

public class DimensionModel {
	int width;
	int height;
	
	public int getWidth() {
		return this.width;
	}
	public int getHeight() {
		return this.height;
	}	
	public void setWidth(int width) {
		this.width = width;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	
}
